                <p class="fs-20 text-white mb-2">After a detailed analysis and audit, we found that they don't have a strong social media presence. The website is not well-designed to capture leads and has limited brand awareness.</p>
                <ul class="mb-lg-5 mb-4">
                    <li class="text-white fs-20">We optimized website UX, page structure, and content to improve engagement and lead generation.</li>
                    <li class="text-white fs-20">Implemented SEO strategy to target admission-related keywords in Dubai and lead generation.</li>
                    <li class="text-white fs-20">Launched display ads and Google search ads to help inform parents searching for admissions.</li>
                    <li class="text-white fs-20">Launched Facebook and Instagram Ads (Meta Ads) to highlight academic programs, events, and student achievements.</li>
                </ul>
